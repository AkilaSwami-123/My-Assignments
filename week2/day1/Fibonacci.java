package week2.day1;

public class Fibonacci {

	public static void main(String[] args) {
		
          int first=0, second=1;
          for(int i=0;i<8;i++)
          { 
        	  int third;
        	  System.out.println(first);
        	  third=first+second;
          	  first=second;
        	  second=third;
		      	  
        	  
          }
        	  
	}

}
